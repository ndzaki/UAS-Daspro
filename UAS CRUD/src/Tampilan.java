    private void jTabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabelMouseClicked
        try {
            databaru = false;
            
            int row = jTabel.getSelectedRow();
            String nama = jTabel.getModel().getValueAt(row, 0).toString();
            
            Connection conn = koneksi.getKoneksi();
            Statement stm = conn.createStatement();
            ResultSet sql = stm.executeQuery("SELECT * FROM tb_biodatadiri WHERE nama='" +nama+ "'");
            
            if (sql.next()) {
                jTextNama.setText(sql.getString("nama"));
                jTextTanggal_Lahir.setText(sql.getString("tgl_lahir"));
                jTextNIM.setText(sql.getString("nim"));
                jTextEmail.setText(sql.getString("email"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Tampilan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jTabelMouseClicked

    private void getData(){
        try {
            Connection conn = koneksi.getKoneksi();
            Statement stm = conn.createStatement();
            ResultSet result = stm.executeQuery("SELECT * FROM tb_biodatadiri");
            
        DefaultTableModel model = (DefaultTableModel) jTabel.getModel();
            
            //reset data tabel
            model.setRowCount(0);
            
                while(result.next()) {
                    String Nama = result.getString("nama");
                    String Tanggal_Lahir = result.getString("tgl_lahir");
                    String NIM = result.getString("nim");
                    String Email = result.getString("email");
                    
                     model.addRow(new Object[]{Nama, Tanggal_Lahir, NIM, Email});
                }
            
        } catch (SQLException ex) {
            Logger.getLogger(Tampilan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
