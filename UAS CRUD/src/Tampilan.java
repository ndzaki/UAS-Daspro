    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
        try {
            String sql = "DELETE FROM tb_biodatadiri WHERE nama='"+ jTextNama.getText() +"'";
            
            Connection conn = koneksi.getKoneksi();
            PreparedStatement pst = conn.prepareStatement(sql);
            
            pst.execute();
            
            JOptionPane.showMessageDialog(null, "Berhasil Dihapus");
            
            NewActionPerformed(null);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Gagal Dihapus" + ex);
        }
        
        getData();
    }//GEN-LAST:event_DeleteActionPerformed

    private void SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveActionPerformed
        // Tombol save
        if(databaru) {
            try {
                String sql = "INSERT INTO tb_biodatadiri VALUES ('%s', '%s', '%s', '%s')";
                
                sql = String.format(sql,
                        jTextNama.getText(),
                        jTextTanggal_Lahir.getText(),
                        jTextNIM.getText(),
                        jTextEmail.getText()
                );
                
                Connection conn = koneksi.getKoneksi();
                PreparedStatement pst = conn.prepareStatement(sql);
                
                pst.execute();
                
                JOptionPane.showMessageDialog(null, "Berhasil Disimpan");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Gagal Disimpan" + ex);
            }
             } else {
            try {
                String sql = "UPDATE tb_biodatadiri SET Nama='%s', Tanggal_Lahir='%s', NIM='%s', Email='%s',";
                
                sql = String.format(sql,
                        jTextNama.getText(),
                        jTextTanggal_Lahir.getText(),
                        jTextNIM.getText(),
                        jTextEmail.getText()
                );
                
                Connection conn = koneksi.getKoneksi();
                PreparedStatement pst = conn.prepareStatement(sql);
                
                pst.execute();
                
                JOptionPane.showMessageDialog(null,"Berhasil Diedit");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null,"Gagal Diedit" + ex);
            }
        }
        
        getData();
    }//GEN-LAST:event_SaveActionPerformed

